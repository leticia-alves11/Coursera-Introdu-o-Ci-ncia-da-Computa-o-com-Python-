import math

x1 = float(input("Digite a coordenada x:"))
y1 = float(input("Digite a coordenada y:"))
x2 = float(input("Digite a segunda coordenada x:"))
y2 = float(input("Digite a seunda coordenada y:"))

distancia = (math.sqrt((x1-x2)**2)+((y1-y2)**2))

if distancia <= 10:
    print("Longe")
else:
        distancia >= 10
        print("Perto")

   
